class Product:
    def __init__(self, id, name, price, stock):
        self.id = id
        self.name = name
        self.price = price
        self.stock = stock

    def id(self):
        return self.id

    def name(self):
        return self.name
    
    def price(self):
        return self.price
    
    def stock(self):
        return self.stock
    
    
   

    